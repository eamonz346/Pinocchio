const dictionaries: Record<WorkbenchLanguage, Record<WorkbenchTextKey, string>> = { zh, en };

export type WorkbenchTranslator = (key: WorkbenchTextKey, vars?: Record<string, string | number>) => string;

export function createTranslator(language: WorkbenchLanguage): WorkbenchTranslator {
  return (key, vars) => format(dictionaries[language][key] ?? zh[key], vars);
}

const fallbackTranslator = createTranslator("zh");

const WorkbenchI18nContext = createContext<{ language: WorkbenchLanguage; t: WorkbenchTranslator }>({
  language: "zh",
  t: fallbackTranslator
});

export function WorkbenchI18nProvider({ language, children }: { language: WorkbenchLanguage; children: ReactNode }) {
  const value = useMemo(() => ({ language, t: createTranslator(language) }), [language]);
  return createElement(WorkbenchI18nContext.Provider, { value }, children);
}

export function useWorkbenchI18n() {
  return useContext(WorkbenchI18nContext);
}

function format(template: string, vars: Record<string, string | number> | undefined) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (match, key: string) => String(vars[key] ?? match));
}
